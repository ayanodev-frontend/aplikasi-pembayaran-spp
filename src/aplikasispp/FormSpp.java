    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
try {
    String sql = "INSERT INTO spp (tahun, nominal) VALUES (?,?)";
    java.sql.Connection conn = aplikasispp.Koneksi.configDB();
    java.sql.PreparedStatement pst = conn.prepareStatement(sql);
    
    pst.setString(1, txtTahun.getText());
    pst.setString(2, txtNominal.getText());
    
    pst.execute();
    javax.swing.JOptionPane.showMessageDialog(null, "Tarif SPP Berhasil Disimpan!");
    tampil_data();
} catch (Exception e) {
    javax.swing.JOptionPane.showMessageDialog(this, e.getMessage());
}
    }//GEN-LAST:event_jButton1ActionPerformed

    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
try {
    String sql ="UPDATE spp SET tahun = ?, nominal = ? WHERE id_spp = ?";
    java.sql.Connection conn = aplikasispp.Koneksi.configDB();
    java.sql.PreparedStatement pst = conn.prepareStatement(sql);
    
    pst.setString(1, txtTahun.getText());
    pst.setString(2, txtNominal.getText());
    pst.setString(3, txtIdSpp.getText()); // Menggunakan ID sebagai kunci
    
    pst.execute();
    javax.swing.JOptionPane.showMessageDialog(null, "Data Tarif SPP Berhasil Diubah!");
    tampil_data();
    kosongkan_form();
} catch (Exception e) {
    javax.swing.JOptionPane.showMessageDialog(null, e.getMessage());
}
    }//GEN-LAST:event_jButton2ActionPerformed

